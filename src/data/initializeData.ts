const groupByUE = (data) => {
  return Object.values(
    data.reduce((a, c) => {
      if (a.hasOwnProperty(c["Code UE"])) {
        a[c["Code UE"]].push(c);
      } else {
        a[c["Code UE"]] = [c];
      }
      return a;
    }, {})
  );
};

import { Ue } from "../entities/ue.entity";
import { Ec } from "../entities/ec.entity";
import { AppDataSource } from "../config/app-data-source";

const UeRepository = AppDataSource.getRepository(Ue);
const EcRepository = AppDataSource.getRepository(Ec);

export const insterdata = async () => {
  const UeCount = await UeRepository.count();
  if (UeCount === 0) {
    let groups: any = groupByUE(data);
    for (const group of groups) {
      let ue = UeRepository.create({
        name: group[0]["Nom UE"],
        code: group[0]["Code UE"],
        mention: group[0]["Mention"],
      });
      await UeRepository.save(ue);
      for (const item of group) {
        let ec = EcRepository.create({
          name: item["Nom EC"],
          ue: ue,
        });
        await EcRepository.save(ec);
      }
    }
  }
};
